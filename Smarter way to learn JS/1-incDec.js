let a = 0;
console.log(a, a++, a);
console.log(a, ++a, a);

// a++ will add 1 to the value but original value will be as it is,
// ++a will increment both the value and its original value.


// same with decrement operator 
let b = 5;
console.log(b, --b, b);
console.log(b, b--, b);


