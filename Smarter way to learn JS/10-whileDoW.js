// for loop 
for(let i=0; i<=5; i++){
    console.log(i);
}

// while loop 
let i = 0;
while (i <=10) {
    console.log(i);
    i++;
}

// do while loop 
let a = 1;
do{
    console.log(a);
    a++;
} while(a<=15);



// difference
// In while loop the code may not executes if the condition is false.
// But in do-while the code always executes once evenif the condition is false.

// Basically while is like for, which checks the condition then executes the code,
// And do-while executes the code the checks the condition.
